
CREATE OR REPLACE PROCEDURE PROD_DB.CORTEX_CHAT_APP.SP_FRAUD_ALERTS_FCT(
    P_WATERMARK_FROM TIMESTAMP_NTZ DEFAULT NULL,
    P_WATERMARK_TO   TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    P_BATCH_ID       VARCHAR       DEFAULT NULL,
    P_BATCH_SIZE     INTEGER       DEFAULT NULL,
    P_DRY_RUN        BOOLEAN       DEFAULT FALSE
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
DECLARE
    v_status        VARCHAR        DEFAULT 'RUNNING';
    v_rows_inserted INTEGER        DEFAULT 0;
    v_rows_updated  INTEGER        DEFAULT 0;
    v_err_msg       VARCHAR;
    v_failed_sql    VARCHAR;
    v_start_ts      TIMESTAMP_NTZ  DEFAULT CURRENT_TIMESTAMP();
BEGIN
    -- ── AUDIT: start ─────────────────────────────────────────────────
    INSERT INTO PROD_DB.CORTEX_CHAT_APP.MIGRATION_AUDIT_LOG
        (MAPPING_NAME, PROCEDURE_NAME, EXEC_START_TS, EXEC_END_TS, STATUS,
         ROWS_INSERTED, ROWS_UPDATED, ROWS_DELETED, INFORMATICA_MAPPING_REF, ERROR_MESSAGE, BATCH_ID)
    VALUES ('FRAUD_ALERT_PIPELINE', 'SP_FRAUD_ALERTS_FCT',
            :v_start_ts, NULL, 'RUNNING',
            0, 0, 0, 'FRAUD_ALERT_PIPELINE', NULL, :P_BATCH_ID);

    -- ── PRE-HOOK ─────────────────────────────────────────────────────
    -- (no pre-SQL declared in lineage; add session params here if needed)

    -- ── MAIN TRANSFORMATION ──────────────────────────────────────────
    -- [INFA: Mapping] FRAUD_ALERT_PIPELINE  →  Target: FRAUD_ALERTS_FCT
    -- Source: UNKNOWN_SOURCE
    -- Load strategy: UPSERT  Truncate: False
    BEGIN
        LET stmt VARCHAR := $$WITH
SQ_FRAUD_ALERT_PIPELINE AS (
  -- [INFA: SourceQualifier] SQ_FRAUD_ALERT_PIPELINE — SQ override SQL
  SELECT * FROM TXN_RAW_SRC WHERE TXN_DT >= $$WATERMARK_FROM AND TXN_DT < $$WATERMARK_TO
),
EXPR_FRAUD_ALERT_PIPELINE AS (
  -- [INFA: Expression] EXPR_FRAUD_ALERT_PIPELINE
  SELECT
    -- [INFA] Complexity=SIMPLE src=None.None → tgt=FRAUD_ALERTS_FCT.ALERT_SK
  -- TODO: VERIFY — MEDIUM confidence mapping
    NULL AS ALERT_SK,
    -- [INFA] Complexity=SIMPLE src=TXN_RAW_SRC.TXN_ID → tgt=FRAUD_ALERTS_FCT.TXN_ID
    -- Expression: TXN_ID
    -- SF rewrite:  TXN_ID
    TXN_ID AS TXN_ID,
    -- [INFA] Complexity=SIMPLE src=TXN_RAW_SRC.ACCT_ID → tgt=FRAUD_ALERTS_FCT.ACCT_ID
    -- Expression: ACCT_ID
    -- SF rewrite:  ACCT_ID
    ACCT_ID AS ACCT_ID,
    -- [INFA] Complexity=SIMPLE src=TXN_RAW_SRC.CUST_ID → tgt=FRAUD_ALERTS_FCT.CUST_ID
    -- Expression: CUST_ID
    -- SF rewrite:  CUST_ID
    CUST_ID AS CUST_ID,
    -- [INFA] Complexity=SIMPLE src=TXN_RAW_SRC.TXN_AMT → tgt=FRAUD_ALERTS_FCT.TXN_AMT
    -- Expression: MAX(TXN_AMT)
    -- SF rewrite:  MAX(TXN_AMT)
    MAX(TXN_AMT) AS TXN_AMT,
    -- [INFA] Complexity=MODERATE src=TXN_RAW_SRC.TXN_DT → tgt=FRAUD_ALERTS_FCT.TXN_DT
    -- Expression: TRUNC(TXN_DT)
    -- SF rewrite:  DATE_TRUNC('DAY', TXN_DT)
    DATE_TRUNC('DAY', TXN_DT) AS TXN_DT,
    -- [INFA] Complexity=MODERATE src=None.None → tgt=FRAUD_ALERTS_FCT.TXN_AMT_BAND
    -- Expression: IIF(NVL(TXN_AMT,0)>=100000,'MEGA',IIF(NVL(TXN_AMT,0)>=10000,'HIGH',IIF(NVL(TXN_AMT,0)>=1000,'MED','LOW')))
    -- SF rewrite:  CASE WHEN COALESCE(TXN_AMT THEN 0)>=100000 ELSE 'MEGA',CASE WHEN (COALESCE(TXN_AMT,0 END>=10000,'HIGH',CASE WHEN COALESCE(TXN_AMT THEN 0)>=1000 ELSE 'MED','LOW' END))
  -- TODO: VERIFY — MEDIUM confidence mapping
    CASE WHEN COALESCE(TXN_AMT THEN 0)>=100000 ELSE 'MEGA',CASE WHEN (COALESCE(TXN_AMT,0 END>=10000,'HIGH',CASE WHEN COALESCE(TXN_AMT THEN 0)>=1000 ELSE 'MED','LOW' END)) AS TXN_AMT_BAND,
    -- [INFA] Complexity=MODERATE src=TXN_RAW_SRC.CHANNEL_CD → tgt=FRAUD_ALERTS_FCT.FRAUD_SCORE
    -- Expression: SUM(IIF(CHANNEL_CD='INT',30,0)+IIF(COUNTRY_CD!='US',25,0)+IIF(TXN_AMT_BAND='MEGA',45,IIF(TXN_AMT_BAND='HIGH',20,0)))
    -- SF rewrite:  SUM(CASE WHEN CHANNEL_CD='INT' THEN 30 ELSE 0 END+CASE WHEN COUNTRY_CD!='US' THEN 25 ELSE 0 END+CASE WHEN TXN_AMT_BAND='MEGA' THEN 45 ELSE CASE WHEN (TXN_AMT_BAND='HIGH',20,0 END))
    SUM(CASE WHEN CHANNEL_CD='INT' THEN 30 ELSE 0 END+CASE WHEN COUNTRY_CD!='US' THEN 25 ELSE 0 END+CASE WHEN TXN_AMT_BAND='MEGA' THEN 45 ELSE CASE WHEN (TXN_AMT_BAND='HIGH',20,0 END)) AS FRAUD_SCORE,
    -- [INFA] Complexity=MODERATE src=TXN_RAW_SRC.COUNTRY_CD → tgt=FRAUD_ALERTS_FCT.FRAUD_REASON
    -- Expression: IIF(FRAUD_SCORE>=80,'HIGH_RISK_COMPOSITE',IIF(FRAUD_SCORE>=40,'MEDIUM_RISK','LOW_RISK'))
    -- SF rewrite:  CASE WHEN FRAUD_SCORE>=80 THEN 'HIGH_RISK_COMPOSITE' ELSE CASE WHEN (FRAUD_SCORE>=40,'MEDIUM_RISK','LOW_RISK' END)
    CASE WHEN FRAUD_SCORE>=80 THEN 'HIGH_RISK_COMPOSITE' ELSE CASE WHEN (FRAUD_SCORE>=40,'MEDIUM_RISK','LOW_RISK' END) AS FRAUD_REASON,
    -- [INFA] Complexity=SIMPLE src=None.None → tgt=FRAUD_ALERTS_FCT.CUST_SEGMENT
  -- TODO: VERIFY — MEDIUM confidence mapping
    NULL AS CUST_SEGMENT,
    -- [INFA] Complexity=SIMPLE src=None.None → tgt=FRAUD_ALERTS_FCT.ACCT_RISK_TIER
  -- TODO: VERIFY — MEDIUM confidence mapping
    NULL AS ACCT_RISK_TIER,
    -- [INFA] Complexity=SIMPLE src=None.None → tgt=FRAUD_ALERTS_FCT.LOAD_TS
  -- TODO: VERIFY — MEDIUM confidence mapping
    NULL AS LOAD_TS
  FROM SQ_FRAUD_ALERT_PIPELINE
),
LKP_CUST_SEG AS (
  -- [INFA: Lookup] LKP_CUST_SEG → CUSTOMER_MASTER
  SELECT
    src.*,
    lkp.CUST_SEGMENT
  FROM   EXPR_FRAUD_ALERT_PIPELINE src
  LEFT   JOIN PROD_DB.CORTEX_CHAT_APP.CUSTOMER_MASTER lkp
      ON lkp.CUST_ID = src.CUST_ID
),
LKP_ACCT_RISK AS (
  -- [INFA: Lookup] LKP_ACCT_RISK → ACCOUNT_RISK_PROFILE
  SELECT
    src.*,
    lkp.ACCT_RISK_TIER
  FROM   LKP_CUST_SEG src
  LEFT   JOIN PROD_DB.CORTEX_CHAT_APP.ACCOUNT_RISK_PROFILE lkp
      ON lkp.ACCT_ID = src.ACCT_ID
),
AGG_FRAUD_ALERT_PIPELINE AS (
  -- [INFA: Aggregator] AGG_FRAUD_ALERT_PIPELINE
  SELECT
    TXN_ID AS TXN_ID  -- GROUP BY key,
    ACCT_ID AS ACCT_ID  -- GROUP BY key,
    CUST_ID AS CUST_ID  -- GROUP BY key,
    CASE WHEN COALESCE(TXN_AMT THEN 0)>=100000 ELSE 'MEGA',CASE WHEN (COALESCE(TXN_AMT,0 END>=10000,'HIGH',CASE WHEN COALESCE(TXN_AMT THEN 0)>=1000 ELSE 'MED','LOW' END)) AS TXN_AMT_BAND  -- GROUP BY key,
    NULL AS ALERT_SK  -- AGG: ,
    MAX(TXN_AMT) AS TXN_AMT  -- AGG: MAX(TXN_AMT),
    DATE_TRUNC('DAY', TXN_DT) AS TXN_DT  -- AGG: MAX(TXN_DT),
    SUM(CASE WHEN CHANNEL_CD='INT' THEN 30 ELSE 0 END+CASE WHEN COUNTRY_CD!='US' THEN 25 ELSE 0 END+CASE WHEN TXN_AMT_BAND='MEGA' THEN 45 ELSE CASE WHEN TXN_AMT_BAND='HIGH' THEN 20 ELSE 0 END END) AS FRAUD_SCORE  -- AGG: SUM(IIF(CHANNEL_CD='INT',30,0)+IIF(COUNTRY_CD!='US',25,0)+IIF(TXN_AMT_BAND='MEGA',45,IIF(TXN_AMT_BAND='HIGH',20,0))),
    CASE WHEN FRAUD_SCORE>=80 THEN 'HIGH_RISK_COMPOSITE' ELSE CASE WHEN FRAUD_SCORE>=40 THEN 'MEDIUM_RISK' ELSE 'LOW_RISK' END END AS FRAUD_REASON  -- AGG: IIF(FRAUD_SCORE>=80,'HIGH_RISK_COMPOSITE',IIF(FRAUD_SCORE>=40,'MEDIUM_RISK','LOW_RISK')),
    NULL AS CUST_SEGMENT  -- AGG: ,
    NULL AS ACCT_RISK_TIER  -- AGG: ,
    NULL AS LOAD_TS  -- AGG: 
  FROM LKP_ACCT_RISK
  GROUP BY TXN_ID, ACCT_ID, CUST_ID, TXN_AMT_BAND
)
-- [INFA: Router] group condition: FRAUD_SCORE >= 80
INSERT INTO PROD_DB.CORTEX_CHAT_APP.FRAUD_ALERTS_FCT
  (ALERT_SK, TXN_ID, ACCT_ID, CUST_ID, TXN_AMT, TXN_DT, TXN_AMT_BAND, FRAUD_SCORE, FRAUD_REASON, CUST_SEGMENT, ACCT_RISK_TIER, LOAD_TS)
SELECT ALERT_SK, TXN_ID, ACCT_ID, CUST_ID, TXN_AMT, TXN_DT, TXN_AMT_BAND, FRAUD_SCORE, FRAUD_REASON, CUST_SEGMENT, ACCT_RISK_TIER, LOAD_TS
FROM   AGG_FRAUD_ALERT_PIPELINE
WHERE  FRAUD_SCORE >= 80;$$;

        IF (NOT P_DRY_RUN) THEN
            EXECUTE IMMEDIATE :stmt;
            v_rows_inserted := SQLROWCOUNT;
        ELSE
            -- DRY RUN mode: return preview query, do not write
            RETURN 'DRY_RUN: ' || :stmt;
        END IF;

        v_status := 'SUCCESS';
    EXCEPTION
        WHEN OTHER THEN
            v_status     := 'FAILED';
            v_err_msg    := SQLERRM;
            v_failed_sql := stmt;
            INSERT INTO PROD_DB.CORTEX_CHAT_APP.MIGRATION_ERROR_LOG
                (PROC_NAME, ERROR_CODE, ERROR_MESSAGE, FAILED_SQL, EXEC_TS, BATCH_ID)
            VALUES ('SP_FRAUD_ALERTS_FCT', SQLCODE, :v_err_msg,
                    :v_failed_sql, CURRENT_TIMESTAMP(), :P_BATCH_ID);
    END;

    -- ── ROW COUNT VALIDATION ─────────────────────────────────────────
    -- Check inserted vs expected; raise if delta > 5%
    -- (Adjust threshold per data volume SLA)

    -- ── AUDIT: end ────────────────────────────────────────────────────
    UPDATE PROD_DB.CORTEX_CHAT_APP.MIGRATION_AUDIT_LOG
    SET    EXEC_END_TS   = CURRENT_TIMESTAMP(),
           STATUS        = :v_status,
           ROWS_INSERTED = :v_rows_inserted,
           ROWS_UPDATED  = :v_rows_updated,
           ERROR_MESSAGE = :v_err_msg
    WHERE  MAPPING_NAME    = 'FRAUD_ALERT_PIPELINE'
      AND  PROCEDURE_NAME  = 'SP_FRAUD_ALERTS_FCT'
      AND  STATUS          = 'RUNNING';

    RETURN :v_status;
END;
$$;
